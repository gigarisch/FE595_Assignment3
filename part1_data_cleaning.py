import re

def cleanTFC(thems, joint = False, lst = False):
    # =============================================================================
    #  Split into list of strings
    # =============================================================================
    thems = re.split(r'\n+',thems)
    # =============================================================================
    # Remove digits
    # =============================================================================
    thems = [''.join([i for i in s if not i.isdigit()]) for s in thems]
    # =============================================================================
    # Remove leading whitespace with period
    # =============================================================================
    thems = [re.sub(r'^\.\s','',i) for i in thems]
    # =============================================================================
    # Remove trailing periods and whitespace
    # =============================================================================
    thems = [re.sub(r'\.\s$|\.$|\s$','',i) for i in thems]
    # =============================================================================
    # Remove leading whitespace with comma
    # =============================================================================
    thems = [re.sub(r'^\,\s','',i) for i in thems]
    # =============================================================================
    # Split between male and female groups
    # =============================================================================
    return ([i for i in thems if re.findall(r'^She\'s',i)], 
             [i for i in thems if re.findall(r'^He\'s',i)])


